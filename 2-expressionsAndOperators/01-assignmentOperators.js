//assignment operator
let r = 5;

//addition assignment operator
r +=1// r= r+1
console.log(r)

//Subtraction assignment operator
r-=1;// r= r - 1
console.log(r)//6

//multipication assignment operator
r*=1
console.log(r)
//division assignment operator
r /= 2;//r = r/2
console.log(r); //2.5

//Remainder (modulus) assignment operator
r%=2;//r = r % 2
console.log(r)//.05

//exponential assignment operator
r**=1// r = r**1
console.log(r)